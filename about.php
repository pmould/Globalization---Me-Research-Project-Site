      <h1> About the Study</h1>
      <p>The world we live in is constantly changing. Every part of the world is currently  experiencing some form of social change and transformation. Globalization is  responsible for bringing about much of the changes taking place around the  world. The processes of globalization affect every aspect of the society including  its economic, social, cultural &amp; political components. The resulting transformations  in each of these sectors affects the lives of people remarkably. </p>
      <p>This research study seeks to assess and analyze some of the effects of  globalization on youth lifestyles &amp; livelihoods in Africa through pictures. </p>
      <p>We invite you to participate in this study by submitting just one picture  based on your own true life experience that shows how one or multiple aspects  of globalization has affected (or  is currently  affecting) your life. The picture can show any aspect of your life which can  include your economic, social, cultural and or political life. </p>
      <p>Participants will be entered into a drawing of 10 cash prizes of $20 each. The pictures submitted will become part of a photo database that will be analyzed to identify how different aspects of globalization affect young people across the continent. Findings from the study will be documented, summarized and shared in different circles including academic and scientific journals and conferences. All names will be removed from photo entries before they are entered into the photo database for analyses. The photos will also be de-identified when research findings are being shared or displayed.</p>
      <p>The pictures you submit can show any impact of globalization. It can be a  positive or negative impact.
